        const menuNames = ['Hot Beverages','Cold Beverages','Main Dishes','Desserts'];
        const menuNameContainer = document.querySelector('.menu-name h2');
        slides.forEach((slide,index)=>{
            if(index !== 0) slide.style.display="none";
        })

        rightArrowBtn.addEventListener('click',()=>{
            slides[currentIndex].style.display="none";
            if(currentIndex === slides.length-1){
                currentIndex = 0;
                slides[currentIndex].style.display="grid";
                menuNameContainer.innerText=menuNames[currentIndex];
                return;
            }
            currentIndex++;
            slides[currentIndex].style.display="grid";
            menuNameContainer.innerText=menuNames[currentIndex];
        })

        leftArrowBtn.addEventListener('click',()=>{
            slides[currentIndex].style.display="none";
            if(currentIndex === 0){
                currentIndex = slides.length-1;
                slides[currentIndex].style.display="grid";
                menuNameContainer.innerText=menuNames[currentIndex];
                return;
            }
            currentIndex--;
            slides[currentIndex].style.display="grid";
            menuNameContainer.innerText=menuNames[currentIndex];
        })
